//mycal.cpp
#include "mycal1.h"

float mycal_add(int num1, int num2)
{
    return (float)num1 + num2;
}

float mycal_min(int num1, int num2)
{
    return (float)num1 - num2;
}

float mycal_mul(int num1, int num2)
{
    return (float)num1 * num2;
}

float mycal_div(int num1, int num2)
{
    return (float)num1 / num2;
}
